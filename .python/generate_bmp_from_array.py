def byte_array_to_bmp(array, bmp_path: str):
    pages = HEIGHT // 8
    img = Image.new("1", (WIDTH, HEIGHT))
    pixels = img.load()

    for page in range(pages):
        for x in range(WIDTH):
            byte = array[page * WIDTH + x]
            for bit in range(8):
                y = page * 8 + bit
                pixel_on = (byte >> bit) & 1
                pixels[x, y] = 255 if pixel_on else 0

    img.save(bmp_path)
    print(f"Saved {bmp_path}")

